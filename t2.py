def ultimoPuntajeRegistrado(puntajes):
    return puntajes[-1]

# Lista de puntajes dada
puntajes = [100, 0, 90, 30]

# Encontrar el último puntaje registrado
ultimo_puntaje = ultimoPuntajeRegistrado(puntajes)

print(f"El último puntaje registrado es: {ultimo_puntaje}")

#El último puntaje registrado es: 30